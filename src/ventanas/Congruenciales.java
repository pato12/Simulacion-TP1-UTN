    private void generarNumeros(int cantidad) {
        if (!setearGenerador()) {
            return;
        }

        for (int i = 0; i < cantidad; i++) {
            double ri = generador.nextNumber() * 9999;
            modelo.addRow(new Object[]{orden++, (int) ri});
        }
    }

    private boolean setearGenerador() {
        if (generador != null) {
            return true;
        }

        try {
            String tipo = getTipoGenerador();
            int x0 = Integer.parseInt(text_x0.getText());

            switch (tipo) {
                case "cl":
                    if ("".equals(text_g.getText())) {
                        int a = Integer.parseInt(text_a.getText());
                        int c = Integer.parseInt(text_c.getText());
                        int m = Integer.parseInt(text_m.getText());

                        generador = new CongruenteLineal(a, c, m, x0);
                    } else {
                        int g = Integer.parseInt(text_g.getText());
                        int k = Integer.parseInt(text_k.getText());
                        generador = new CongruenteLineal(x0);
                        generador.setOptimo(k, g);
                    }
                    break;
                case "cm":
                    if ("".equals(text_g.getText())) {
                        int a = Integer.parseInt(text_a.getText());
                        int m = Integer.parseInt(text_m.getText());

                        generador = new CongruenteMultiplicativo(a, m, x0);
                    } else {
                        int g = Integer.parseInt(text_g.getText());
                        int k = Integer.parseInt(text_k.getText());
                        generador = new CongruenteMultiplicativo(x0);
                        generador.setOptimo(k, g);
                    }
                    break;
                default:
                    throw new Error("Tipo de generador invalido.");
            }
            setEnabledCampos(false);
            button_retry.setEnabled(true);
            button_freccuencia.setEnabled(true);
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error! Corrobore los datos y vuelva a intentar.");
            generador = null;
            setEnabledCampos(true);
            button_retry.setEnabled(false);
            button_freccuencia.setEnabled(false);
            orden = 1;

            return false;
        }

    }

    private void setEnabledCampos(boolean enabled) {
        text_a.setEnabled(enabled);
        text_c.setEnabled(enabled);
        text_m.setEnabled(enabled);
        text_g.setEnabled(enabled);
        text_k.setEnabled(enabled);
        text_x0.setEnabled(enabled);

        for (Enumeration<AbstractButton> buttons = tipoGenerador.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            button.setEnabled(enabled);
        }
    }

    private String getTipoGenerador() {
        for (Enumeration<AbstractButton> buttons = tipoGenerador.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getActionCommand();
            }
        }

        return null;
    }

